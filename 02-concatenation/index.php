<?php
// concatenation
$firstName = 'John';
$lastName = 'Doe';
$fullName = $firstName . " " . $lastName;

echo $fullName;

echo "<hr>";

// string interpolation
$userFirstName = "John";
$userLastName = "Doe";
echo "Hello, $userFirstName $userLastName!";

echo "<hr>";

// escape character
echo 'I\'m John Doe';
